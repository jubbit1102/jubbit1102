T = int(input())

for x in range(1, T+1):
    N, Q = map(int, input().split())

    # 상자에 적혀있는 값들(처음에는 다 0으로 시작)
    value = [0] * N
    for i in range(1, Q + 1):    # Q회 만큼 시행
        L, R = map(int, input().split())  #  Q개의 줄의 i번째 줄에는 Li, Ri (1 ≤ Li ≤ Ri ≤ N)이 주어진다.
        value[L-1:R] = [i] * (R - L + 1)  # 슬라이싱으로 해도 되나 싶어서 한번 강행해봤다.
                                          # 중간에 이렇게 해도 되나 싶어서 효련 언니한테 물어봤는데
                                          # 언니는 for 문을 한번 더 쓰는 형식으로 코딩했다.
                                          # 그래서 그냥 GPT 선생님한테 물어봤는데
        # 슬라이스 할당 시 타입 오류
        # value[L:R] = i 같은 슬라이스 할당은 리스트 전체 부분에 값 여러 개를 할당할 때 사용합니다. 그런데 i는 정수 하나이므로 한 번에 여러 개 요소에 복사할 수 없습니다.
        # 즉, value[L:R] = i는 TypeError를 발생시킵니다.
        #
        # 인덱스 범위 문제 (1-based vs 0-based)
        # 입력값 L, R이 1부터 시작하는데, 파이썬 리스트 인덱스는 0부터 시작합니다. 따라서 슬라이스 할당할 때는 value[L-1:R] 식으로 맞춰줘야 합니다.

        # ====> 라는 실수를 찾았다... 아직도 범위 설정을 헷갈려 한다... 더 꼼꼼하게 공부해야 한다

    print(f'#{x} {" ".join(map(str, value))}')
    # 처음에 그냥 {value}만 썼을 때는 리스트 형식으로 나와서 곤란했다
    # 문자열로 바꿔야 하는데 join이 생각이 안 나서 다 해놓고 또 GPT 도움 받았다..
